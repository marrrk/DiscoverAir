/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	HAL_StatusTypeDef ret;
	uint8_t receive_data[4];
	uint8_t sgp_receive_data[3];
	uint8_t send_data =0x00;
	uint8_t send_data_1 =0x00;



	uint8_t sgpc3_measure_tvoc[2] = { 0x20, 0x08 };
	uint8_t sgpc3_tvoc_init_continuous[2] = { 0x20, 0xae };

	uint16_t temp_x,humi_x;
	float* temperature=0;
	uint8_t* humidity=0;
	static const uint8_t HDC1080_ADDR = 0x40 << 1; // Use 8-bit address
	static const uint8_t SGPC3_ADDR = 0x58 << 1;
	uint8_t txbuf[64];
	uint8_t txbuf_1[64];


  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  MX_I2C2_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		  //Demo sending data over UART2 to Laptop
	      debugPrintln(&huart2, "Dean\n"); // print full line
		  ret = HAL_I2C_Master_Transmit(&hi2c2, HDC1080_ADDR,&send_data, 1, 1000);
		  HAL_Delay(1000);
		  if ( ret != HAL_OK ) {
		  			     debugPrintln(&huart2, "Error 0"); // print full line
		  			     HAL_Delay(1000);
		  			    }
		  /* Delay here 15ms for conversion compelete.
		  	 * Note: datasheet say maximum is 7ms, but when delay=7ms, the read value is not correct
		  	 */

		  else{
		  ret = HAL_I2C_Master_Receive(&hi2c2,HDC1080_ADDR,receive_data,4,1000);
		  if ( ret != HAL_OK ) {
		  		  			     debugPrintln(&huart2, "Error 1"); // print full line
		  		  			     HAL_Delay(1000);
		  		  			    }
		  else{
		  temp_x =((receive_data[0]<<8)|receive_data[1]);
		  humi_x =((receive_data[2]<<8)|receive_data[3]);
          float temperature1= ((temp_x/65536.0)*165.0)-40.0;
          //*temperature=temperature1;
		  float humidity=((humi_x/65536.0)*100.0);

		  //debugPrintln(&huart2, "Temperature 2 is %f\n"); // print full line
		    sprintf((char*)txbuf, "Temperature: %f\r\n", temperature1);
		    HAL_UART_Transmit(&huart2, txbuf, strlen((char*)txbuf), HAL_MAX_DELAY);

		    sprintf((char*)txbuf_1, "Humidity: %f\r\n", humidity);
		    HAL_UART_Transmit(&huart2, txbuf_1, strlen((char*)txbuf), HAL_MAX_DELAY);
		  }
		     }
		     ret = HAL_I2C_Master_Transmit(&hi2c1,SGPC3_ADDR,&sgpc3_tvoc_init_continuous, 2, 1000);
		     HAL_Delay(1000);
		     if ( ret != HAL_OK ) {
		     			     debugPrintln(&huart2, "Error 20"); // print full line
		     			     HAL_Delay(1000);
		     			    }
			 ret = HAL_I2C_Master_Transmit(&hi2c1,SGPC3_ADDR,sgpc3_measure_tvoc, 2, 1000);
			 HAL_Delay(1000);
			  if ( ret != HAL_OK ) {
			     debugPrintln(&huart2, "Error 2"); // print full line
			     HAL_Delay(1000);
			    }
			   else{
				   ret=HAL_I2C_Master_Receive(&hi2c1, SGPC3_ADDR,sgp_receive_data, 3, 1000);
				  		  if ( ret != HAL_OK ) {
				  		      debugPrintln(&huart2, "Error 3"); // print full line
				  		      HAL_Delay(1000);
				  		      }
				  		   else{
				  		      HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_8);
				  		      HAL_Delay(1000);
				  		    }
			       }
			    }


    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

  /* USER CODE END 3 */
}

